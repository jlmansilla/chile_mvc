
class User < ApplicationRecord
  # == Attributes =================================================================================

  # == Constants ==================================================================================

  # == Class Methods ==============================================================================

  # == Extensions =================================================================================

  # == Instance Methods ===========================================================================

  # == Relationships ==============================================================================
  belongs_to :country, optional: true
  # == Scopes =====================================================================================

  # == Validations ================================================================================
  validates :country_valid, acceptance: {message: 'The country must be a valid country'}
  # == Privates  ==================================================================================
  private
   
  def country_valid
    return true if country_id.blank?
    Country.where(id: country_id).any? ? true : false
  end
end


